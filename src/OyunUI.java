    private void LoginToServer(){
        try {
            String host = "localhost";
            int port = 44444;
            client = new TCP_Client();
            client.start(host, port, GeneralChatPane, kAdıTxt);
            
            client.sendMessage("@zorluk-"+ZorlukSeviyesi);
            client.setEnableBtn(StartGame);
        } catch (IOException ex) {
            System.out.println("IoException on LoginToServer at OyunUI");
        }
    }

