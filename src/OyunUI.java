    private void SendAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendAnswerActionPerformed
        try {
            
            if (client.isOnGame()){
            client.sendMessage("@cevap-"+jTextField1.getText());
            client.writeToHistory("Cevabınız : " + this.jTextField1.getText());
            jTextField1.setText("");
            }else{
            client.sendMessage(jTextField1.getText());
            
            jTextField1.setText("");
            }
        } catch (IOException ex) {
            Logger.getLogger(OyunUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SendAnswerActionPerformed

    private void StartGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartGameActionPerformed
        try {
            // TODO add your handling code here:
            if (client.isOnGame() == false){
            client.sendMessage("@start-");
            client.setOnGame(true); 
            StartGame.setEnabled(false);
            }
        } catch (IOException ex) {
            Logger.getLogger(OyunUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_StartGameActionPerformed

